package com.gx.po;

public class UserAlbumCommentStutsPo {
	
	private int id;
	private String userName;
	private String mail;
	private String albumCommentStuts;
	private int albumCommentId;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getMail() {
		return mail;
	}
	public void setMail(String mail) {
		this.mail = mail;
	}
	public String getAlbumCommentStuts() {
		return albumCommentStuts;
	}
	public void setAlbumCommentStuts(String albumCommentStuts) {
		this.albumCommentStuts = albumCommentStuts;
	}
	public int getAlbumCommentId() {
		return albumCommentId;
	}
	public void setAlbumCommentId(int albumCommentId) {
		this.albumCommentId = albumCommentId;
	}

}
