package com.gx.po;

public class UserSongCommentStutsPo {
	
	private int id;
	private String userName;
	private String mail;
	private String songCommentStuts;
	private int songCommentId;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getMail() {
		return mail;
	}
	public void setMail(String mail) {
		this.mail = mail;
	}
	public String getSongCommentStuts() {
		return songCommentStuts;
	}
	public void setSongCommentStuts(String songCommentStuts) {
		this.songCommentStuts = songCommentStuts;
	}
	public int getSongCommentId() {
		return songCommentId;
	}
	public void setSongCommentId(int songCommentId) {
		this.songCommentId = songCommentId;
	}

	

}
