package com.gx.po;

public class AlbumPo {
	
	private int id;
	
	private String albumName;
	
	private String singerName;
	
	private String albumPhotoPath;
	
	private String isPopular;
	
	private String country;
	
	private String schools;
	
	private String year;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getAlbumName() {
		return albumName;
	}

	public void setAlbumName(String albumName) {
		this.albumName = albumName;
	}

	public String getSingerName() {
		return singerName;
	}

	public void setSingerName(String singerName) {
		this.singerName = singerName;
	}

	public String getAlbumPhotoPath() {
		return albumPhotoPath;
	}

	public void setAlbumPhotoPath(String albumPhotoPath) {
		this.albumPhotoPath = albumPhotoPath;
	}

	public String getIsPopular() {
		return isPopular;
	}

	public void setIsPopular(String isPopular) {
		this.isPopular = isPopular;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getSchools() {
		return schools;
	}

	public void setSchools(String schools) {
		this.schools = schools;
	}

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}
	
	

}
