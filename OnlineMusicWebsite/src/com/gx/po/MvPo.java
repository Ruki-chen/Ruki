package com.gx.po;

public class MvPo {
	
	private int id;
	
	private String mvName;
	
	private String singerName;
	
	private String songName;
	
	private String mvPath;
	
	private String isPopular;
	
	private String country;
	
	private String schools;
	
	private String year;
	
	private String mvPhotoPath;
	
	

	public String getMvPhotoPath() {
		return mvPhotoPath;
	}

	public void setMvPhotoPath(String mvPhotoPath) {
		this.mvPhotoPath = mvPhotoPath;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getMvName() {
		return mvName;
	}

	public void setMvName(String mvName) {
		this.mvName = mvName;
	}

	public String getSingerName() {
		return singerName;
	}

	public void setSingerName(String singerName) {
		this.singerName = singerName;
	}

	public String getSongName() {
		return songName;
	}

	public void setSongName(String songName) {
		this.songName = songName;
	}

	public String getMvPath() {
		return mvPath;
	}

	public void setMvPath(String mvPath) {
		this.mvPath = mvPath;
	}

	public String getIsPopular() {
		return isPopular;
	}

	public void setIsPopular(String isPopular) {
		this.isPopular = isPopular;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getSchools() {
		return schools;
	}

	public void setSchools(String schools) {
		this.schools = schools;
	}

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

}
